class Calculos:
    def __init__(self,num1,num2):
        self.num1 = num1
        self.num2 = num2

    def Somar(self):
        return self.num1 + self.num2

    def Subtrair(self):
        return self.num1 - self.num2

    def Dividir(self):
        return self.num1 / self.num2

    def Multiplicar(self):
        return self.num1 * self.num2

